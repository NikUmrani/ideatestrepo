package com.company;

public class DataTypes_and_Operators {

    public static void main(String[] args)
    {
        int a=15;
        double b=25.658;
        boolean c=true;
        String s="Nikhil";
        char d='k';
        byte e=127;//Max limit

        System.out.println(a);
        System.out.println(b*40.5);
        System.out.println(c);
        System.out.println(s);
        System.out.println(d);
        System.out.println(e);


        // Operators

        int x=50;
        int y=86;

        System.out.println(x+y);
        System.out.println(x*y);
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x%y);//Arithmatic Operators



        System.out.println(x<y);
        System.out.println(x>y);
        System.out.println(x==y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x!=y);//Relational Operators

        boolean t=true;
        boolean u=false;

        System.out.println(t&&u);
        System.out.println(t||u);
        System.out.println(!t);
        System.out.println(!u);

        int num;
        num=a;
        System.out.println(num);
        num=x;
        System.out.println(num);

        num=100;
        num++;
        System.out.println(num);
        num=100;
        num--;
        System.out.println(num);
        num=100;
        num+=5;
        System.out.println(num);
        num=100;
        num-=5;
        System.out.println(num);
        num=100;
        num*=5;
        System.out.println(num);
        num=100;
        num/=5;
        System.out.println(num);














    }




}
